#include "Main.h"
#include "2_01_12h.h"
#include "ReadKeyBoard.h"

/* ............................................................... */
/* ............................................................... */

void main(void)
{
    // Set up the scheduler	设置调度器
    hSCH_Init_T2();

    LED_Short_Init();

    hSCH_Add_Task(LED_Short_Update, 0, 1000, 0);


    hSCH_Add_Task(LED_Long_Update, 0, 20000, 1);

    // Start the scheduler
    hSCH_Start();

    while(1) {
        hSCH_Dispatch_Tasks();
    }
}

